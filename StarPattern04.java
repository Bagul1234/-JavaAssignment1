package Pattern_java;

public class StarPattern04 {

	public static void main(String[] args) {
		int rows=6;
		int star=6;
		//int spaces=4;
		for(int i=0;i<=rows;i++)
		{
			for(int j=0;j<=star;j++) {
				System.out.print("*");
			}System.out.println();
			star--;
			//spaces--;
		
	}

}

}


