package Pattern_java;

public class star_pattern {

	public static void main(String[] args) {
		int rows=6;
		int star=1;
		//int spaces=4;
		for(int i=1;i<=rows;i++)
		{
			for(int j=1;j<=star;j++) {
				System.out.print("*");
			}System.out.println();
			star++;
			//spaces--;
		
	}

}
}
